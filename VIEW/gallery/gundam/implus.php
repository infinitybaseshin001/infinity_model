                                <br><br>
                                ○マスターグレードの1/100　MGフォースインパルスにノワールストライカーを取り付けソードインパルスガンダムとして 製作。
                                <br><br>
                                ■　基本工作　■<br>
                                ゲート処理、ヒケ処理、モールドの彫り直し後にサフ→ベースホワイト→ 着色→スミイレ→デカール貼り→つや消しコートの順です。
                                <br><br>
                                ■頭部<br>
                                モールドの彫り直し。<br>
                                頭部の継ぎ目の処理は段落ちモールドを追加。
                                <br><br>
                                ■胸部<br>
                                モールドの彫り直しとパーツ同士の間隔がタイトなのでクリアランス処理。<br>
                                適所にピンバイスで穴をあけ1.5mmスチールボールを使用してディテールを追加。
                                <br><br>
                                ■肩部<br>
                                適所にピンバイスで穴をあけ1.5mmスチールボールを使用してディテールを追加。
                                <br><br>
                                ■腰部<br>
                                ○フロントアーマー<br>
                                適所にピンバイスで穴をあけスチールボールを使用してディテールを追加。<br>
                                ○リアアーマー<br>
                                適所にピンバイスで穴をあけスチールボールを使用してディテールを追加。<br>
                                ○サイドアーマー<br>
                                適所にピンバイスで穴をあけスチールボールを使用してディテールを追加。<br>
                                各パーツはすべてモールドの彫り直しをしています。
                                <br><br>
                                ■脚部<br>
                                サイドのアンクルガードにあるスリットはプラ板でふさぎ、シンプルなデザインに変更。<br>
                                内部のシリンダーはメッキシルバーで塗装。<br>
                                シリンダーノズルはブライトゴールドで塗装しています。 
